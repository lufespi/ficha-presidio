package entities;

public class Ethnicity {
  private int id;
  private String etnia;

  public int getId() {
    return id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public String getEtnia() {
    return etnia;
  }
  public void setEtnia(String etnia) {
    this.etnia = etnia;
  }
}